def generate(transcript: str, analysis: str, platform: str) -> str | None:
    """Generate a platform script via Gemini Flash. Returns text or None."""
    if not GEMINI_API_KEY:
        logger.warning("GEMINI_API_KEY not set, skipping generation")
        return None
    if platform not in _PLATFORM_PROMPTS:
        logger.warning("Unknown platform: %s", platform)
        return None
    try:
        genai.configure(api_key=GEMINI_API_KEY)
        model = genai.GenerativeModel(
            "gemini-2.0-flash",
            system_instruction=_SYSTEM_PROMPT,
        )
        user_prompt = _PLATFORM_PROMPTS[platform].format(
            analysis=analysis[:2000],
            transcript=transcript[:6000],
        )
        response = model.generate_content(user_prompt)
        return response.text.strip() or None
    except Exception as e:
        logger.warning("Gemini generation failed for %s: %s", platform, e, exc_info=True)
        return None
